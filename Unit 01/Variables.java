public class ExampleVariables  { // make sure the class name is the same as filename
    public static void main(String []args) { // main program
      
      int someInteger = 42;
      double someFloat = 3.14;
      double someDouble = 3.14159265359;

      char oneChar = 'a';
      String someString = "Hello World";
      
      boolean oneBoolean = true;

      System.out.println(someInteger);
      System.out.println(someFloat);
      System.out.println(someDouble);
      System.out.println(oneChar);
      System.out.println(someString);
      System.out.println(oneBoolean);

    }
 }